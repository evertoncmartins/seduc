# 1. Crie uma lista de números inteiros
numeros = [10, 20, 30, 40, 50]

# 2. Inicialize uma variável para armazenar a soma
soma = 0

# 3. Use um loop for para iterar sobre os elementos da lista
for numero in numeros:
    # 4. Adicione o elemento atual à variável de soma
    soma += numero

# 5. Exiba a soma total
print("A soma total dos elementos da lista é:", soma)
