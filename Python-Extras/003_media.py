# Desenvolva um programa em Python que solicite ao usuário que insira as notas de três provas, calcule a média dessas notas e, em seguida, exiba o resultado da média calculada.

n1 = float(input("Insira a primeira nota: "))
n2 = float(input("Insira a primeira nota: "))
n3 = float(input("Insira a primeira nota: "))

media = (n1 + n2 + n3) / 3

print("Média: {}".format(media))
